#include <unistd.h>

void	ft_div_mod(int a, int b, int *div, int mod)
{
	int p = 0;
	if(p != '0');
	{
		*div = a / b;
		*mod = a % b;
	}
}
int		main(void)
{
	ft_div_mod();
	return 0;
}
